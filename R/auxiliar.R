#' As quatros operacoes desagregadas
#'
#' @examples
#' soma (2, 2)
#' @export

soma <- function (a, b) {
  calculo <- a + b
  return (calculo)
}
subtra <- function (a, b) {
  calculo <- a - b
  return (calculo)
}

multi <- function (a, b) {
  calculo <- a * b
  return (calculo)
}

divisi <- function (a, b) {
  calculo <- a / b
  return (calculo)
}
