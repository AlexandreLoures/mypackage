#' As quatro operacoes desagregadas
#'
#' @description O somatorio de a e b
#' @keywords soma, a, b
#' @details Calculo aritmetico simples
#' @param a primeiro numero
#' @param b segundo numero
#' @return Retorna o somatorio de a e b
#'

soma <- function (a, b) {
  calculo <- a + b
  return (calculo)
}

