#' As quatros operacoes desagregadas
#'
#' @examples
#' soma (2, 2)
soma <- function (a, b) {
  calculo <- a + b
  return (calculo)
}
#' subtra (4, 2)
subtra <- function (a, b) {
  calculo <- a - b
  return (calculo)
}
#' multi (3, 3)
multi <- function (a, b) {
  calculo <- a * b
  return (calculo)
}
#' divisi (8, 4)
divisi <- function (a, b) {
  calculo <- a / b
  return (calculo)
}
